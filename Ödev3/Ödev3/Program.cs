int sayac = 1;

do
{
    Console.WriteLine("Sayaç değeri: " + sayac);
    sayac++;
} while (sayac <= 5);

Console.WriteLine("Döngü tamamlandı");

/* Do-while döngüsü, C# dilinde bulunan bir döngü 
 * yapısıdır. While döngüsüne benzerdir, ancak önemli bir farkı vardır:
 * Do-while döngüsünde, döngü bloğu içindeki kodlar en az bir kez çalıştırılır, daha sonra koşul kontrol edilir.
 */


